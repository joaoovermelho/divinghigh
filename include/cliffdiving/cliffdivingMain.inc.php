                    <h4 class="pl-130">A ciência por trás</h4>
                    <h2 class="title-3 pl-110 pt-20 mb-60">Cliff Diving</h2>
                    <p>Como muitos desportos, o Cliff Diving é também frequentemente chamado uma arte. Mas, por trás dessa arte está muita ciência e números. </p>
                    <p>Temos, como exemplo a altura, a velocidade e a força gravitacional, bem como a consciência aérea, o tempo e a força física, todos estes desempenham papeis fundamentais na criação de um mergulho perfeito, e claro, seguro.</p>
                    <p>Aqui estão uns pequenos fatos rápidos sobre a ciência do Cliff Diving:</p>
                    <p>•	Salto de decolagem - Até 0,8m</p>
                    <p>•	Velocidade de rotação - 2,4 por segundo</p>
                    <p>•	Velocidade vertical - 22,5 m por segundo</p>
                    <p>•	Tempo no ar - 2,6 segundos  </p>
                    <p>•	Entrada de água - Até 85km/h</p>
                    <p>•	Desaceleração de impacto - 10G</p>
                    <p>Quando se trata de construir os mergulhos, os atletas passam muitas horas a experimentar novas formas de realizar certos conjuntos e habilidades.</p>
