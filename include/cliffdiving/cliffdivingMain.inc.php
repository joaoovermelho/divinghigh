                    <p>Queda livre de até 27 metros combinadas com acrobacia impressionantes - essa é a essência do Cliff Diving.</p>
                    <p>Desde 2009, a Red Bull Cliff Diving World Series fornece uma plataforma para ação estética e mergulhos de incrível complexidade. É puro. É de tirar o fôlego e está cheio de drama emocionante.</p>
                    <p>12 homens e 12 mulheres competem a cada paragem, cada um então ganhando pontos cruciais no campeonato ao longo do caminho com base nas suas posições finais no evento. No final de cada temporada, um campeão é coroado em ambas as categorias e ambos recebem os troféus "King Kahekili".</p>
